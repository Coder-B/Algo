from typing import List
class Solution:
    def increasingTriplet(self, nums: List[int]) -> bool:
        if 3 > len(nums):
            return False
        min1,min2 = 2**31-1, 2**31-1
        for i in range(len(nums)):
            item = nums[i]
            if item <= min1:
                min1 = item
            elif item <= min2:
                min2 = item
            else:
                return True
        return False

